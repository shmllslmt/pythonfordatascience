#the argument is the size of loop
print("Given only one argument in range(5)")
for i in range(5):
    print("Jimmy Five Times (" + str(i) + ")")

#1st is min, 2nd is max
print("\nGiven only two argument in range(5, 10)")
for i in range(5, 10):
    print("Jimmy Five Times (" + str(i) + ")")

#1st is min, 2nd is max, 3rd is step value
print("\nGiven three argument in range(2, 25, 3)")
for i in range(2, 25, 3):
    print("Jimmy Five Times (" + str(i) + ")")